<h3 >Criação dos Conselhos de Políticas Públicas </h3>



<div id="divAlinhaImg1" >

                     <p>Com a Constituição da República Federativa do Brasil, em 5 de outubro 1988, se estabeleceu a participação da comunidade nas ações e serviços
                     de saúde e na gestão prática do Ensino Público, surgindo támbem as Leis federais como a Lei Orgânica da Assistência Social e o Estatuto da 
                     Criança e do Adolescente-ECA que possibilitaram a criação dos diversos Conselhos Municipiais brasileiros.<br><br>
                     <strong>A Lei Orgânica Municipal de 1990 (art 78-79.80)</strong> em Campo Grande-MS previu a criação dos Conselhos Municipais como
                     orgãos governamentais com a finalidade de auxiliar a administração pública, na orientação, no planejamento e 
                     interpretação de matéria de sua competência. Estabeleceu que os Conselhos Municipais fossem criados por Lei específica, definindo as competências
                     de cada um, organização, paridade na composição, funcionamento, forma de nomeação e posse de seus membros titulares e suplentes e o prazo de duração
                     de seus mandatos!</p>

