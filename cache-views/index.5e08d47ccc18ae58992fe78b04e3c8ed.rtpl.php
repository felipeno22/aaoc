<h3>Importância  da Assessoria para a sociedade brasileira</h3>

<p>Considerando que a Coordenadoria de Apoio aos Orgãos Colegiados-CAOC (Assessoria de Assistência aos Órgãos Colegiados-AAOC) foi criada pela <strong>Lei n°2951 de 27/07/1993</strong>  como órgão de acolhimento,encaminhamento e busca de soluções para as questões relacionadas as atividades dos níveis de atuação auxiliar, de execução programática, com atribuições definidas na  sua lei de criação.</p>

<p>O tema  "A atuação do Conselho Municipal do Meio Ambiente-CMMA, no licenciamento ambiental de Campo Grande-MS, entre os anos de 1999 até 2007" escolhido pelo 
Presidente do CMMA, foi baseado  através das consultas às atas desta época(1999-2007) e que constam no arquivo desta Assessoria. No arquivo esta registrado  
a prestimosa colaboração da Assessoria às plenárias da então Coordenadoria de Apoio aos Órgãos Colegiados-CAOC (Assessoria de Assistência aos Órgãos 
Colegiados-AAOC), da Secretaria Municipal de Governo e Relações Institucionais-SEGOV, em seu impecável arquivo de memória do Conselho Municipal do Meio Ambiente-CMMA.</p>


<p>Nesse mesmo período, uma acadêmica do Curso de Engenharia Ambiental de Brasília-DF, buscou contato com este setor público, ou seja, esta Assessoria para obter
as informações desse Procedimento de licenciamento ambiental, destacando a iniciativa inédita desta cidade mediante publicação de <strong>Resolução N°237 de 19/12/1997</strong> do Conselho Nacional do Meio Ambiente-CONANDA, da esfera federal, para todos os municipios do país e que isso  motivou em Campo Grande-MS a criação do Sistema Municipal de Licenciamento 
e Controle Ambiental-SILAN.</p>

<p>O SILAN foi criado pela <strong>Lei N°3612 de 30/04/1999</strong>, do qual o Conselho Municipal do Meio Ambiente-CMMA faz parte, reforçando a descentralizacão do poder local nas responsabilidades sobre as questões ambientais pertinentes ao território  de Campo Grande-MS, estabelecendo normas gerais para o licenciamento de loteamentos e 
desmembramentos de nosso munícipio.</p>

<p>Acâdemicos do Curso de Ciências Sociais da Universidade Federal de Mato Grosso do Sul-UFMS, em visita a Assessoria no ano de 2002, buscaram conhecimento sobre o 
funcionamento dos Conselhos Municípais e fizeram pesquisa como exigência parcial para conclusão da disciplina "Introdução à Sociologia e a Teoria Sociológica" e 
constatou que em Campo Grande-MS se apresentavam como um campo previlegiado para o aprendizado da democracia e a reconstrução da politica dado à proximidade que a 
esfera local dispunha com os cidadãos.</p>



<p>Os acadêmicos Eduardo Ramirez Meza,Maribel Alves e William de Oliveira Martins os quais optaram por estudar e entrevistar três conselheiros municipais da cidade,
sendo um do Conselho Municipal de Saúde-CMS, outro do Conselho Municipal de Assistência Social-CMAS e um do Conselho Municipal da Criança e do Adolescente-CMDCA,
conselhos estes de natureza consultiva e deliberativa.</p>

<p>A entrevista foi conduzida com o intuito de desvendar questões como: <br>
   a) Como se deu o processo de discussão das competências, papeís, composições, plano de ação, forma de escolha dos representantes da sociedade civil?<br> 
   b) Quais ações foram previstas e realizadas?<br>
   c) Quais debates públicos foram promovidos?<br> 
   d) Qual a forma de controle observada, sobre os recursos aplicados e sobre as ações do Poder Público?<br>
   e) Qual o grau de articulação com os diversos segmentos sociais?<br>
   f) Em que medida seus componentes discutem previamente com suas entidades, as pautas e proposições das reuniões?<br> 
   g) Até que ponto os Conselhos têm sido mecânismos de fortalecimento da sociedade civil e controle social do Estado?</p>

<p>Ao final do trabalho os acadêmicos concluiram que apesar de estarem vinculados a estrutura administrativa e que suas decisões precisam ser homologadas pelo Poder 
Executivo, os Conselhos funcionam de forma autônoma e democrática. No entanto o seu funcionamento é regido por regras e procedimentos formulados e aprovados por 
seus membros, a autonomia das decisões depende da correlação de forças na estrutura de poder , da organização da sociedade para acompanhar, avaliar e dar suporte
as ações propostas.</p>





<h3 >Os valores dos Conselhos de Políticas Públicas para sociedade brasileira</h3>

<div id="divAlinhaImg4">
                     
                     <p>Os acadêmicos observaram que os Conselhos Municipais analisados nasceram sob a coordenação estatal de governo, mas a coordenação de seus funcionamentos detem a tarefa de promever a articulação dos diferentes aspectos e decisões, dando organicidade as ações do grupo como um todo. E na medida em que o grupo se engaja, aprende com o trabalho e passa a ser capaz para se auto dirigir. Concluindo, os Conselhos são espaços de discussão, de negociação, de articulação e oferecem oportunidades e novos desafios.<br><br>

                     Essa realidade deve ser compreendida como um processo dinâmico, pois na medida em que se melhora a compreensão da realidade pelo trabalho cooperativo,
                     pela descoberta de relações não visiveis nos fatos em si, pela interpretacão dos indicadores de interdependência presentes nos fenômenos sociais, as pessoas e os grupos organizam os seus pensamentos, discutem as ideologias interessadas separando a compreensão da realidade e da necessidade de sua transformação.</p>     


