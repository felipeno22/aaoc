<p>Compreendeu a equipe de estudantes que cabe à comunidade a tarefa de entender como a principal "peça" que compõe a realidade social,  exigindo um alto grau de
comprometimento de todos e por todos, ou seja, um forte sentido de lealdade comunitária, pois onde houver dificuldade individual, o debate coletivo, prevalece, 
