<h3> A estrutura de arquivo de Atas e Posses da Assessoria.</h3>

<p>Finalmente em 08/11/2021 o Vereador Otávio Trad este visitando o Conselho Municipal das Feiras Livres-CMFL, e a convite da chefe da Assessoria conheceu as instalações
