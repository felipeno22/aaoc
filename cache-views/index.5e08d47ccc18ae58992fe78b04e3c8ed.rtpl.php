estabelecimento de políticas públicas.</p>


<h3 >Primeiro Conselho criado antes da Constituição de 1988</h3>



<p>Faz-se necessário destacar que o Conselho Municipal de Desenvolvimento e Urbanização-CMDU ja funcionava desde o ano de 1987 com o objetivo de possibilitar 
a participação da sociedade cívil nas discussões referentes a Política de Desenvolvimento e a Política Urbana do Município de Campo Grande-MS como órgão consultivo,
fiscalizador e proponente da Prefeitura Municipal criado pelo <strong>Decreto nº5484 de 09/03/1987</strong>, se apresentando então como o primeiro Conselho Municipal criado, seguido pelos demais colegiados criados e em funcionamento neste município, criados por leí ou por Decreto Municipal.</p>

<p>No entanto este Conselho hoje por força de normas federais mudou sua nomenclatura quando Campo Grande-MS decidiu readequar seu primeiro Plano Diretor elaborado
primeiramente em 1990. Então, em 2021 sofreu suas primeiras adequação e modificou a nomenclatura Conselho Munícipal de Desenvolvimento e Urbanização-CMDU para 
Conselho Munícipal da Cidade, manteve sua sigla aumentou seu número de assentos entendendo que outros segmentos da sociedade civil organizada precisavam compor 
este Conselho que funciona hoje regularmente em nossa cidade.</p>


