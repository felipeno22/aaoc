            <p>Prosseguindo com o seu papel de Assessoria o secretário Estadual de Cultura do Estado de Mato Grosso do Sul em 1998 solicitou à Coordenadoria hoje, Assessoria de Assistência aos Orgãos Colegiados -AAOC ministrar curso para aproximadamente 30(trinta) municípios em Corumbá Mato Grosso do Sul, possibilitando e orientando que estes criassem seus Conselhos Municípais de Cultura nos moldes daquele criado neste município de Campo Grande-MS.</p>

            <p>Acâdemicos do Curso de Ciências Sociais da Universidade Federal de Mato Grosso do Sul-UFMS, em visita a Assessoria nos anos de 2002 buscaram conhecimento sobre o funcionamento dos Conselhos Municípais e fizeram pesquisa como exigência parcial para conclusão da disciplina <strong>"Introdução à Sociologia e a Teoria Sociológica"</strong> e constatou que em Campo Grande-MS se apresentavam como um campo previlegiado para o aprendizado da democracia e a reconstrução da politica dado à proximidade que a esfera local dispunha com os cidadãos.</p> 

            <p>Assim fizeram entrevistas entre os Conselhos de Políticas Públicas, de garantia e de defesa de direitos, Conselho estes de natureza deliberativa, endagando como se deve se efetivar a formação desses colegiados,competências forma de escolha dos representantes da sociedade civil organizada, controle sobre os recursos aplicados e sobre as ações do Poder Público, grau de articulação com os diversos segmentos sociais, sobre os itens de pauta se eram ou não decididos previamente pelo pleno, concluindo que os Conselhos funcionam de forma autonôma embora seu fucionamento são regidos por regras,regimentos internos por eles elaborado onde procedimentos são formulados e aprovados pelo seus membros, tendo portanto suas autonomias nas decisões, a visto que os Conselhos se reúnem em espaço de discussão de negociação, de articulação, oferecendo oportunidades e novos desafios.</p>

            <p>Nessa linha de ação em abril de 2011 a Assessoria de Assistência aos Orgãos Colegiados-AAOC, recebeu a visita de Assessores da Presidência da República de Brasilia-DF, os senhores José Mauro Cassimiro e José Claudeonor, ambos da Secretária Nacional de Estudos e Pesquisas Políticas Sociais que por informações de conselheiros do Conselho Municipal de Saúde-CMS de Campo Grande-MS que motivou-os a conhecerem a Coordenadoria hoje Assessoria de Assistência aos Orgãos Colegiados-AAOC. ressaltaram que esta era conhecida como <strong>"referência nacional"</strong>.</p>



