              <p>Nesta direção visando garantir uma gestão democrática também modificou sua nomenclatura para Assessoria de Assistência aos Órgãos Colegiados-AAOC e vem contribuindo com este processo democrático garantido pela nossa Carta Magna, a Constituição Federal de 1988 foi regulamentada pelo <strong>decreto n°14.191 de 17/03/2020</strong> que dispõe sobre nova composição do Conselho Municipal da Cidade-CMDU com 18 vagas para Executivo Municipal, 10 vagas para os movimentos populares, 3 vagas para trabalhadores por suas entidades sindicais, 7 vagas para empresários relacionados à produção, 5 vagas para entidades profissionais,acadêmica, de pesquisa e conselhos profissionais e por fim 1 vaga para organização não governamental, somando um total de 44 assentos.</p>

