               <p> Com a Constituição da República Federativa do Brasil, em 5 de outubro 1988, se estabeleceu a participação da comunidade nas ações e serviços de saúde e na gestão prática do Ensino Público, surgindo támbem as Leis federais como a Lei Orgânica da Assistência Social e o Estatuto da Criança e do Adolescente-ECA que possibilitaram a criação dos diversos Conselhos Municipiais brasileiros.</p>

              <p>A Lei Orgânica Municipal de 1990 (art 78-79.80) em Campo Grande-MS previu a criação dos Conselhos Municipais como orgãos governamentais com a finalidade de auxiliar a administração pública, na orientação, no planejamento e interpretação de matéria de sua competência. Estabeleceu que os Conselhos Municipais fossem criados por Lei específica, definindo as competências de cada um, organização, paridade na composição, funcionamento, forma de nomeação e posse de seus membros titulares e suplentes e o prazo de duração de seus mandatos!</p>

              <p>Ainda disciplinou que os Conselhos Municipais fossem compostos por números de membros definidos por Lei devendo a Câmara Municipal aprovar <strong>"ad referedum"</strong> a indicação de seus nomes, observando a representatividade da administracão das entidades públicas, classistas e da sociedade civil organizada, facultando a realização de audiência com a comissão pertinente. Atribuiu ao Chefe do Executivo Municipal nomear os membros titulares e suplentes indicados pelas entidades representativas para os respectivos Conselhos.</p>

              <p>Normatizou que os Conselhos Municipais se articulassem com o Poder Legislativo, participando das reuniões das comissões permanentes da Câmara Municipal de Campo Grande-MS, representados por um de seus membros como convidados ou por convocação.E nessa linha de ação foram criados diversos Conselhos Municipais da cidade que hoje funcionam seguindo a legislação procedente,
              ao então presidente do Conselho Municipal do Meio Ambiente-CMMA de Campo Grande-MS, conselheiro, médico veterinário, Sr.João Vieira de Almeida Neto.
              </p>
              <!--end page 1-->

              <!--page 2-->


              <p>Cujo o tema escolhido foi <strong>"A atuação do Conselho Municipal do Meio Ambiente-CMMA, no licenciamento ambiental de Campo Grande-MS, do ano 1999 até 2007, feito através das consultas às atas desta época e que constam no arquivo desta Assessoria e lá registrou"</strong> a prestimosa colaboração da Assessoria às plenárias da então Coordenadoria de Apoio aos Órgãos Colegiados-CAOC hoje Assessoria de Assistência aos Órgãos Colegiados-AAOC, da Secretaria Municipal de Governo e Relações Institucionais-SEGOV, com seu impecável arquivo de memória do Conselho Municipal do Meio Ambiente-CMMA.</p>

              <p>Nesse mesmo período, uma acadêmica do Curso de Engenharia Ambiental de Brasília-DF, buscou contato com este setor público, ou seja, esta Assessoria para obter as informações desse Procedimento de licenciamento ambiental, destacando a iniciativa inédita desta cidade mediante publicação de <strong>Resolução N°237 de 19/12/1997</strong> do Conselho Nacional do Meio Ambiente-CONANDA, da esfera federal, para todos os municipios do país e aqui em nossa cidade motivou a criação do Sistema Municipal de Licenciamento e Controle Ambiental-SILAN.</p>

              <p>
                 Criado pela <strong>Lei N°3612 de 30/04/1999</strong>, do qual o Conselho Municipal do Meio Ambiente-CMMA, faz parte, reforçando a descentralizacão do poder local nas responsabilidades sobre as questões ambientais pertinentes ao território municipal, estabelecendo normas gerais para o licenciamento de loteamentos e desmembramentos de nosso munícipio.
              </p>

              <p>Ressalta que um trabalho de pesquisa de acadêmicos do Curso de Ciências Sociais, da Universidade Federal de Mato Grosso do Sul-UFMS em 2002 analisou e acompanhou o funcionamento de alguns Conselhos Municipais e realizou como exigência parcial para conclusão da Disciplina <strong>"Introdução a Sociologia e a ---- Sociológica"</strong> e no caso específico se verificou que o municipio de Campo Grande-MS se apresentava como um campo privilegiado para o aprendizado da Democracia e a reconstrução da política, dada à proximidade que a esfera local dispõe com os cidadãos.
              </p>

              <p>
              Recebeu os acadêmicos Eduardo Ramirez Meza,Maribel Alves e William de Oliveira Martins os quais optaram por estudar e entrevistar três conselheiros municipais da cidade, sendo dois de politicas tais como, o Conselho Municipal de Saúde-CMS e o Conselho Municipal de Assistência Social-CMAS e um de garantia e defesa de direitos, como o Conselho Municipal da Criança e do Adolescente-CMDCA, conselhos estes de natureza consultiva e deliberativa.</p> 

              <p>E a entrevista foi conduzida com o intuito de desvendar questões como: Como se deu o processo de discussão das competências, papel, composição, plano de ação, forma de escolha dos representantes da sociedade civil, que ações foram previstas e realizadas, que debates públicos foram promovidos? Qual a forma de controle observada sobre os recursos aplicados e sobre as ações do Poder Público? Qual o grau de articulação com os diversos segmentos sociais? Em que medida seus componentes discutem previamente com suas entidades, as pautas e proposições das reuniões? Por fim observou até que ponto os Conselhos têm sido mecânismos de fortalecimento da sociedade civil e controle social do Estado?.</p>


              <p> Ao final do trabalho os acadêmicos concluiram que apesar de estarem vinculados a estrutura administrativa e que suas decisões precisam ser homologadas pelo Poder Executivo, os Conselhos funcionam de forma autônoma e democrática. No entanto o seu funcionamento é regido por regras e procedimentos formulados e aprovados por seus membros, a autonomia das decisões depende da correlação de forças na estrutura de poder , da organização da sociedade para acompanhar, avaliar e dar suporte as ações propostas.</p>
              <!--end page 2-->

              <!--page 3-->
              <p>Observou-se que os Conselhos Municipais analisados nasceram sob a coordenação estatal de governo, mas a coordenação de seus funcionamentos detem a tarefa de promever a articulação dos diferentes aspectos e decisões, dando organicidade as ações do grupo como um todo. E na medida em que o grupo se engaja, aprende com o trabalho e passa a ser capaz para se auto dirigir. Entendeu-se que os Conselhos são espaços de discussão, de negociação, de articulação e oferecem oportunidades e novos desafios.</p>

              <p>Essa realidade deve ser compreendida como um processo dinâmico, pois na medida em que se melhora a compreensão da realidade pelo trabalho cooperativo, pela descoberta de relações não visiveis nos fatos em si, pela interpretacão dos indicadores de interdependência presentes nos fenômenos sociais, as pessoas e os grupos organizam os seus pensamentos, discutem as ideologias interessadas separando a compreensão da realidade e da necessidade de sua transformação.</p>


              <p>Compreendeu a equipe de estudantes que cabe à comunidade a tarefa de entender-se como a principal <strong>"peça"</strong> que compõe a realidade social, decorrendo dai a exigência de alto grau de comprometimento de todos e por todos, ou seja, um forte sentido de lealdade comunitária, pois onde houver dificuldade individual, o debate coletivo, prevalece, sempre que necessário.</p>

              <p>Concluíu a equipe de acadêmicos que no contexto histórico atual,político,econômico e social devam ser ajustadas as fórmulas adequadas ao exercicio do controle social, onde os Conselhos configuram-se como possíveis causas de mediação dos interesses coletivos e das políticas governamentais, como espaços adequados para o estabelecimento de políticas públicas.</p>

              <p>Faz-se necessário destacar que o Conselho Municipal de Desenvolvimento e Urbanização-CMDU ja funcionava desde o ano de 1987 com o objetivo de possibilitar a participação da sociedade cívil nas discussões referentes a Política de Desenvolvimento e a Política Urbana do Município de Campo Grande-MS como órgão consultivo, fiscalizador e proponente da Prefeitura Municipal criado pelo <strong>Decreto nº5484 de 09/03/1987</strong>, se apresentando então como o primeiro Conselho Municipal criado, seguido pelos demais colegiados criados e em funcionamento neste município, criados por leí ou por Decreto Municipal.</p>

              <p>No entanto este Conselho hoje por força de normas federais mudou sua nomenclatura quando Campo Grande-MS decidiu readequar seu primeiro Plano Diretor elaborado primeiramente em 1990. Então, em 2021 sofreu suas primeiras adequação e modificou o Conselho Munícipal de Desenvolvimento e Urbanização-CMDU para Conselho Munícipal da Cidade, manteve sua sigla aumentou seu número de assentos entendendo que outros segmentos da sociedade civil organizada precisavam compor este Conselho que funciona hoje regularmente em nossa cidade.</p>

              <p>Outros colegiados então foram criados neste munícipio em 1990 o Conselho Munícipal de Saúde-CMS criado pela <strong>Lei n°2784 de 27/12/1990</strong>; sendo seguido pelo Conselho Munícipal dos Direitos da Criança e do Adolescente-CMDCA, pela <strong>Lei nº2892 de 09/07/1992</strong>; o Conselho Munícipal da Cultura-CMC criado pela <strong>Lei n°3017 de 01/12/1993</strong> e que hoje também modificou sua nomenclatura passando a compor a Assessoria como Conselho Munícipal de Políticas Culturais-CMPC.</p>


              <p>O Conselho Munícipal de Apoio à Pessoa Portadora de Deficiência-COMPD foi criado pela <strong>Lei nº3036 de 21/03/1994</strong>, hoje também dispõe de outra nomenclatura, Conselho Munícipal de Apoio a Pessoa com Deficiência-COMPD. Em seguida a <strong>Lei n°3108 de 20/12/1994</strong> criou o Conselho Munícipal de Assistência Social-CMAS, o Conselho Munícipal do Meio Ambiente-CMMA foi criado pela <strong>Lei n°3176 de 11/06/1995</strong>. E o Conselho Munícipal de Alimentação Escolar-CMAE foi criado pela <strong>Lei n°3177 de 11/07/1995</strong>; o Conselho Munícipal de Turismo-CONTUR pela <strong>Lei n°3974 de 12/07/1994</strong>.</p>


              <p>A que se considerar que a Coordenadoria de Apoio aos Orgãos Colegiados-CAOC foi criada pela <strong>Lei n°2951 de 27/07/1993</strong> como órgão de acolhimento,encaminhamento e busca de soluções para as questões relacionadas as atividades dos níveis de atuação auxiliar, de execução programática, com atribuições definidas na sua lei de criação.</p>

